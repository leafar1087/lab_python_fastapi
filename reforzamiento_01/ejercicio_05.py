"""5. Crear una nueva lista vacía y agregar 4 ítems de diferentes tipos de datos."""

# Crear una lista vacía
nueva_lista = []

# Agregar 4 ítems (diferentes tipos de datos)
nueva_lista.append(3.14)    # float
nueva_lista.append(2.71)    # float
nueva_lista.append(1.618)   # float
nueva_lista.append(42)      # int
nueva_lista.append("Python") # string

# Mostrar la nueva lista
print("Nueva lista:", nueva_lista)

